    public boolean validarNombre(){
        boolean res=false;
        Pattern patron = Pattern.compile("[a-zA-Z ]");
        Matcher m = patron.matcher(tfNombre.getText());
         if (m.find())
            res=true;
        
        return res;
    } 
    
    public boolean validarApellido(){
        boolean res=false;
        Pattern patron = Pattern.compile("[a-zA-Z ]");
        Matcher m = patron.matcher(tfApellido.getText());
         if (m.find())
            res=true;
        
        return res;
    } 
    
     public boolean validarTelefono(){
        boolean res=false;
        Pattern patron = Pattern.compile("^4[0-9]{6}$");
        Matcher m = patron.matcher(tfTelefono.getText());
         if (m.find()){
             res=true;
         }
         
        patron = Pattern.compile("^[7,6][0-9]{7}$");
        m = patron.matcher(tfTelefono.getText());
        
        if(m.find()){
             res=true;
         }
         
        
        return res;
    }
     public boolean validarCi(){
        boolean res=false;
        Pattern patron = Pattern.compile("[0-9]");
        Matcher m = patron.matcher(tfCi.getText());
        if (m.find()){
            res=true;
        }
                
        return res;
    }
     
    public boolean validarSalario(){
        boolean res=false;
        Pattern patron = Pattern.compile("^\\d+|^\\d+\\.?\\d+");
        Matcher m = patron.matcher(tfSalario.getText());
        if (m.find()){
            res=true;
        }
                
        return res;
    }
    
    public boolean validarCorreo(){
        boolean res=false;
        Pattern patron = Pattern.compile( "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
            + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher m = patron.matcher(tfCorreo.getText());
        if (m.find()){
            res=true;
        }
                
        return res;
    }
     
    public boolean validador(){
        boolean res = true;
        if(validarNombre()&&!tfNombre.getText().equals("")){
            if(validarApellido()&&!tfApellido.getText().equals("")){
                if(validarCi()&&!tfCi.getText().equals("")){
                        if(!tfDireccion.getText().equals("")){
                                if(validarTelefono()&&!tfTelefono.getText().equals("")){
                                        
                                    if(validarSalario() && !tfSalario.getText().equals("")){
                                        if(validarCorreo() && !tfCorreo.getText().equals("")){
            
                                        }else{
                                            res=false;
                                        JOptionPane.showMessageDialog(null, "Verifique el correo");
                                        }    
                                        
                                        
                                    }else{
                                        res=false;
                                        JOptionPane.showMessageDialog(null, "Verifique el Salario");
                                    }
                                    
                                   }else{
                                    res=false;
                                    JOptionPane.showMessageDialog(null, "Verifique el Telefono");
                                    }
                        }else{
                            res=false;
                            JOptionPane.showMessageDialog(null, "Verifique el Direccion");
                        }
                 }else{
                    res=false;
                    JOptionPane.showMessageDialog(null, "Verifique el C.I.");
                }
            }else{
                res=false;
                JOptionPane.showMessageDialog(null, "Verifique el Apellido");
        
            }
        }else{
            res=false;
            JOptionPane.showMessageDialog(null, "Verifique el Nombre");
        }
        
        return res;
    }
    

