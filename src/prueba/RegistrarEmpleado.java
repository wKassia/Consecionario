    private void tfSalarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSalarioKeyTyped
         char c = evt.getKeyChar();
                if(((c < '0') || (c > '9'))   && (c !='.')){
                /* lo que deseo colocar aqui es si ya se pulso el caracter (.) el mismo no se pueda repetir*/
                    evt.consume();
                }
                if (c == '.' && tfSalario.getText().contains(".")) {
                    evt.consume();
                }
                
                
    }//GEN-LAST:event_tfSalarioKeyTyped

